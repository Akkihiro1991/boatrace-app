export function getInRateLabel(rate) {
  if (rate >= 58) return { label: 'イン最強', color: 'text-yellow-400' }
  if (rate >= 53) return { label: 'イン有利', color: 'text-green-400' }
  if (rate >= 48) return { label: 'イン普通', color: 'text-blue-300' }
  if (rate >= 44) return { label: 'イン不利', color: 'text-orange-400' }
  return { label: 'イン危険', color: 'text-red-400' }
}

export function getSurfaceColor(surface) {
  if (surface.includes('超荒れ')) return 'text-red-400'
  if (surface.includes('荒れやすい')) return 'text-orange-400'
  if (surface.includes('やや荒れ')) return 'text-yellow-400'
  if (surface.includes('超静水')) return 'text-blue-400'
  return 'text-green-400'
}
